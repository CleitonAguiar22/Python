def cauculo(var1 = int(input("Digite um número inteiro: ")), var2 = 5):
    soma = var1 + var2,
    subtração = var1 - var2,
    divisão = var1 / var2,
    multiplicação = var1 * var2,
    restoDaDivisão = var1 % var2

    return print(f'Soma: {soma}, Subtração: {subtração}, Multiplicação: {multiplicação}, Divisão: {divisão}, Resto: {restoDaDivisão}')

cauculo()